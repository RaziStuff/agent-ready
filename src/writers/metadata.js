export function repoMap(scan) {
  return {
    schemaVersion: scan.schemaVersion,
    generatedAt: scan.generatedAt,
    root: scan.root,
    summary: scan.summary,
    inventory: scan.inventory,
    config: scan.config,
    languages: scan.languages,
    packageManagers: scan.packageManagers,
    monorepo: scan.monorepo,
    frameworks: scan.frameworks,
    directories: scan.directories,
    entrypoints: scan.entrypoints,
    docs: scan.docs,
    ci: scan.ci,
    generatedHints: scan.generatedHints
  };
}

export function commandCatalog(scan) {
  return {
    schemaVersion: scan.schemaVersion,
    generatedAt: scan.generatedAt,
    commands: scan.commands
  };
}

function packageManagerForWorkspaceCommands(scan) {
  return scan.packageManagers.find((manager) => ["pnpm", "yarn", "npm"].includes(manager.name))?.name ?? "npm";
}

function packageScriptCommand({ packageManager, workspacePackage, scriptName }) {
  if (packageManager === "pnpm") {
    return `pnpm --filter ${workspacePackage.name} ${scriptName}`;
  }
  if (packageManager === "yarn") {
    return `yarn workspace ${workspacePackage.name} ${scriptName}`;
  }
  if (packageManager === "npm") {
    return scriptName === "test"
      ? `npm test --workspace ${workspacePackage.path}`
      : `npm run ${scriptName} --workspace ${workspacePackage.path}`;
  }
  return `npm run ${scriptName} --prefix ${workspacePackage.path}`;
}

export function workspaceCatalog(scan) {
  const packageManager = packageManagerForWorkspaceCommands(scan);
  const commonScripts = ["test", "lint", "typecheck", "build", "dev"];
  const packages = scan.monorepo.packages.map((workspacePackage) => ({
    ...workspacePackage,
    commands: commonScripts
      .filter((scriptName) => workspacePackage.scripts.includes(scriptName))
      .map((scriptName) => ({
        name: scriptName,
        command: packageScriptCommand({ packageManager, workspacePackage, scriptName }),
        source: `${workspacePackage.path}/package.json:scripts.${scriptName}`
      }))
  }));

  return {
    schemaVersion: scan.schemaVersion,
    generatedAt: scan.generatedAt,
    root: scan.root,
    summary: {
      purpose: scan.summary.purpose,
      primaryLanguage: scan.summary.primaryLanguage,
      monorepo: scan.summary.monorepo,
      packageManagers: scan.summary.packageManagers,
      frameworks: scan.summary.frameworks,
      packageCount: packages.length
    },
    monorepo: {
      detected: scan.monorepo.detected,
      kind: scan.monorepo.kind,
      tools: scan.monorepo.tools,
      workspaceGlobs: scan.monorepo.workspaceGlobs,
      packageCount: packages.length
    },
    commands: {
      workspace: scan.commands.filter((command) => command.name.startsWith("workspace ")),
      packageScoped: packages.flatMap((workspacePackage) => (
        workspacePackage.commands.map((command) => ({
          packagePath: workspacePackage.path,
          packageName: workspacePackage.name,
          ...command
        }))
      ))
    },
    packages
  };
}

export function riskPolicy(scan) {
  const lines = [
    "# Agent Risk Policy",
    "",
    "Generated by agent-ready. Treat this as a starting point and edit it with repo-specific guidance.",
    "",
    "## Risk Areas",
    ""
  ];

  if (scan.risks.length === 0) {
    lines.push("_No risk areas were detected by path._");
  } else {
    for (const risk of scan.risks) {
      lines.push(`- \`${risk.path}\` (${risk.severity}): ${risk.reason}.`);
    }
  }

  lines.push("");
  lines.push("## Default Agent Rules");
  lines.push("");
  lines.push("- Do not read or expose secret-like files.");
  lines.push("- Avoid hand-editing generated outputs.");
  lines.push("- Treat migrations, infra, deployment, and CI changes as high-impact.");
  lines.push("- Keep lockfile changes scoped to dependency changes.");
  lines.push("- Run the smallest relevant validation before handing off.");
  lines.push("");

  return `${lines.join("\n")}\n`;
}

