export function agentIndex(scan) {
  const lines = [
    "# Agent Index",
    "",
    "This file is a longer generated index for agents that want more structured context.",
    "",
    "## Languages",
    ""
  ];

  if (scan.languages.length === 0) {
    lines.push("- No language signals detected.");
  } else {
    for (const language of scan.languages) {
      lines.push(`- ${language.name}: ${language.fileCount} files, confidence ${language.confidence}.`);
    }
  }

  lines.push("");
  lines.push("## Commands");
  lines.push("");
  if (scan.commands.length === 0) {
    lines.push("- No commands detected.");
  } else {
    for (const command of scan.commands) {
      lines.push(`- ${command.name}: \`${command.command}\` from ${command.source}.`);
    }
  }

  lines.push("");
  lines.push("## Workspaces");
  lines.push("");
  if (!scan.monorepo.detected) {
    lines.push("- No workspace layout detected.");
  } else if (scan.monorepo.packages.length === 0) {
    lines.push("- Workspace tooling detected, but no workspace packages were matched.");
  } else {
    for (const workspacePackage of workspaceCatalog(scan).packages.slice(0, 50)) {
      const commands = workspacePackage.commands.length > 0
        ? workspacePackage.commands.map((command) => `\`${command.command}\``).join(", ")
        : "no package-scoped commands";
      lines.push(`- \`${workspacePackage.path}/\`: \`${workspacePackage.name}\`; ${commands}.`);
    }
  }

  lines.push("");
  lines.push("## Evidence");
  lines.push("");
  for (const item of scan.evidence.slice(0, 100)) {
    lines.push(`- ${item}`);
  }
  lines.push("");

  return `${lines.join("\n")}\n`;
}

async function writeJson(filePath, data) {
  await fs.writeFile(filePath, `${JSON.stringify(data, null, 2)}\n`, "utf8");
}

export async function writeMetadata({ root, scan, dryRun = false }) {
  const agentsDir = path.join(root, ".agents");
  const files = [
    { path: path.join(agentsDir, "repo-map.json"), kind: "json", data: repoMap(scan) },
    { path: path.join(agentsDir, "commands.json"), kind: "json", data: commandCatalog(scan) },
    { path: path.join(agentsDir, "workspaces.json"), kind: "json", data: workspaceCatalog(scan) },
    { path: path.join(agentsDir, "risk-policy.md"), kind: "text", data: riskPolicy(scan) },
    { path: path.join(agentsDir, "handoff-template.md"), kind: "text", data: handoffTemplate() },
    { path: path.join(agentsDir, "agent-index.md"), kind: "text", data: agentIndex(scan) }
  ];

  if (dryRun) {
    return {
      wrote: false,
      files: files.map((file) => file.path)
    };
  }

  await fs.mkdir(agentsDir, { recursive: true });
  for (const file of files) {
    if (file.kind === "json") {
      await writeJson(file.path, file.data);
    } else {
      await fs.writeFile(file.path, file.data, "utf8");
    }
  }

  return {
    wrote: true,
    files: files.map((file) => file.path)
  };
}

const PROFILE_FILES = new Map([
  ["claude", "CLAUDE.md"],
  ["cursor", ".cursorrules"],
  ["aider", "CONVENTIONS.md"],
  ["openhands", ".openhands/AGENTS.md"]
]);

export async function writeProfilePointers({ root, profiles = [], force = false, dryRun = false }) {
  const written = [];
  const skipped = [];

  for (const profile of profiles) {
    const normalized = profile.trim().toLowerCase();
    if (!normalized || normalized === "generic" || normalized === "codex") {
      continue;
    }

    const relPath = PROFILE_FILES.get(normalized);
    if (!relPath) {
      skipped.push({ profile, reason: "unknown profile" });
      continue;
    }

    const absolute = path.join(root, relPath);
    const content = "# Agent Instructions\n\nRead `AGENTS.md` first. It is the canonical agent operating guide for this repo.\n";
    if (dryRun) {
      written.push(absolute);
      continue;
    }

    if ((await pathExists(absolute)) && !force) {
      skipped.push({ profile, reason: `${relPath} already exists` });
      continue;
    }

    await fs.mkdir(path.dirname(absolute), { recursive: true });
    await fs.writeFile(absolute, content, "utf8");
    written.push(absolute);
  }

  return { written, skipped };
}
